// 活动图
// 在UML里，活动图本质上就是流程图，
// 它描述系统的活动，判断系统的活动，判断点和分支等。

// 时序图
// 时序图强调消息时间顺序的交互图
// 时序图用于描述类系统中类与类之间的交互，以及交互建模（换）成消息交换
// 时序图用于描述对象之间如何随着时间进行协作
// 时序图由活动者（Actor）、对象(Object)、 消息(Message)、生命线(LifeLine)和控制焦点（Focus Of Control）组成
// 不同元素有不同表示
//    对象是一个矩形，对象名称有下划线
//    消息用由方向的箭头表示，调用是实线，返回消息是虚线
//    生命线由纵向的虚线表示
//    控制焦点是纵向的矩形，也就是活动条（Activation Bar） 【这个时间点它是活动的】
